package com.lightspeedworks.node.json;

/**
 * JSON Type. {JSON型}
 *
 * @author LightSpeedC (Kazuaki Nishizawa; 西澤 和晃)
 */
public enum JSONType {
	/**
	 * Number type. {数値型}
	 */
	NUMBER,
	/**
	 * String type. {文字列型}
	 */
	STRING,
	/**
	 * Boolean type. {ブーリアン型}
	 */
	BOOLEAN,
	/**
	 * Array type. {配列型}
	 */
	ARRAY,
	/**
	 * Object type. {オブジェクト型}
	 */
	OBJECT,
	/**
	 * Null type. {ヌル型}
	 */
	NULL;
}
